export const getAiCodeHelp = async (projectFiles: ProjectFile[], activeFile: ProjectFile, prompt: string): Promise<string | { html: string; css: string; js: string; }> => {
    try {
        if (activeFile.type === 'html') {
            const htmlFile = projectFiles.find(f => f.type === 'html');
            const cssFile = projectFiles.find(f => f.type === 'css');
            const jsFile = projectFiles.find(f => f.type === 'js');

            const fullPrompt = `The user wants to modify a single-file HTML application based on this request: "${prompt}".

Here is the current code for the project:

File: index.html
\`\`\`html
${htmlFile?.content || ''}
\`\`\`

File: style.css
\`\`\`css
${cssFile?.content || ''}
\`\`\`

File: script.js
\`\`\`javascript
${jsFile?.content || ''}
\`\`\`

Based on the user's request, please provide the complete, new, and fully functional code for all three files.
If a file does not need to be changed, return its original content.
Your response must be a valid JSON object.`;

            const response = await ai.models.generateContent({
                model: 'gemini-2.5-pro',
                contents: fullPrompt,
                config: {
                    systemInstruction: `You are an expert code assistant. Your goal is to help the user by modifying their code based on their request. Return a JSON object with three keys: "html", "css", and "js". Each key should contain the complete, raw code for the corresponding file. Do not include any explanations or markdown formatting like \`\`\` in your output.`,
                    responseMimeType: "application/json",
                    responseSchema: {
                        type: Type.OBJECT,
                        properties: {
                            html: { type: Type.STRING, description: 'The full, updated HTML code for index.html.' },
                            css: { type: Type.STRING, description: 'The full, updated CSS code for style.css.' },
                            js: { type: Type.STRING, description: 'The full, updated JavaScript code for script.js.' },
                        },
                        required: ['html', 'css', 'js'],
                    },
                },
            });

            try {
                return JSON.parse(response.text);
            } catch (e) {
                console.error("Failed to parse AI JSON response:", response.text);
                throw new Error("AI returned invalid JSON. Please try again.");
            }

        } else { // For CSS or JS files
            const language = activeFile.type === 'js' ? 'JavaScript' : 'CSS';
            const fullPrompt = `User request: "${prompt}"

Current ${language} Code (${activeFile.name}):
\`\`\`${activeFile.type}
${activeFile.content}
\`\`\`
`;
            const response = await ai.models.generateContent({
                model: 'gemini-2.5-pro',
                contents: fullPrompt,
                config: {
                    systemInstruction: `You are an expert ${language} code assistant. Your goal is to help the user by modifying their code based on their request.
Analyze the user's request and the provided code context to understand the desired change, which might include adding new features, fixing bugs, or refactoring the code structure.
Return the **complete, modified file content for ${activeFile.name}**.
Do not add any explanations, comments, or markdown formatting like \`\`\`${activeFile.type}.
Only output the raw, updated code for the entire file.`
                },
            });
            
            let code = response.text.trim();
             if (code.startsWith('```')) {
                const lines = code.split('\n');
                if (lines.length > 2) {
                    code = lines.slice(1, lines.length - 1).join('\n');
                } else {
                    code = code.substring(code.indexOf('\n') + 1, code.lastIndexOf('```')).trim();
                }
            }
            return code;
        }

    } catch (error) {
        console.error("AI code help error:", error);
        throw error;
    }
};

